public class Main {
    public static void main(String[] args) {

        for (int i = 2; i < 10; i += 2) {
            System.out.println(i);
        }
        System.out.println("For Döngüsu Bitti");

        int i = 2;

        while (i < 10) {
            System.out.println(i);
            i += 2;
        }
        System.out.println("While Dongusu Bitti");

        int j = 2;
        do {

            j+=2;
            System.out.println(j);

        } while (j < 10);

        System.out.println("Do While donguus bitti");

    }
}